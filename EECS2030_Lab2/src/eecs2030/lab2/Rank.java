package eecs2030.lab2;

/**
 * The thirteen ranks of cards in a standard 52-card French deck. This
 * enumeration orders the ace as the highest rank;
 * the ranks in order from "smallest" rank to "greatest" rank is:
 * 
 * <ul>
 * <li><code>TWO</code>
 * <li><code>THREE</code>
 * <li><code>FOUR</code>
 * <li><code>FIVE</code>
 * <li><code>SIX</code>
 * <li><code>SEVEN</code>
 * <li><code>EIGHT</code>
 * <li><code>NINE</code>
 * <li><code>TEN</code>
 * <li><code>JACK</code>
 * <li><code>QUEEN</code>
 * <li><code>KING</code>
 * <li><code>ACE</code>
 * </ul>
 *
 */
public enum Rank {
	
	/**
	 * The rank 2.
	 */
	TWO,
	
	/**
	 * The rank 3.
	 */
	THREE,
	
	/**
	 * The rank 4.
	 */
	FOUR,
	
	/**
	 * The rank 5.
	 */
	FIVE,
	
	/**
	 * The rank 6.
	 */
	SIX,
	
	/**
	 * The rank 7.
	 */
	SEVEN,
	
	/**
	 * The rank 8.
	 */
	EIGHT,
	
	/**
	 * The rank 9.
	 */
	NINE,
	
	/**
	 * The rank 10.
	 */
	TEN,
	
	/**
	 * The rank jack.
	 */
	JACK,
	
	/**
	 * The rank queen.
	 */
	QUEEN,
	
	/**
	 * The rank king.
	 */
	KING,
	
	/**
	 * The rank ace.
	 */
	ACE
}
